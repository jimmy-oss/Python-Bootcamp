# Else

# The else statement comes right at the end of if statement
# It is used to execute code if the if statement is false

height = 54 #inches
if height > 70 :
  print("You're really tall")
else:
  print("You're not really tall")
  
# The height is not greater than 70 
# so the if statement evaluates to False
# and the else statement is called
